x = 1:280;
y = zeros(4,280);
y(:,1)=190;  
for i=2:280
    y(1,i) = y(1,i-1) + 0.007111 * rand(1) + (2 * rand(1)-1) * 3.95 / 280;
    y(2,i) = y(2,i-1) + 0.007111 * rand(1) + (2 * rand(1)-1) * 4.95 / 280;
    y(3,i) = y(3,i-1) + 0.006111 * rand(1) + (2 * rand(1)-1) * 5.95 / 280;
    y(4,i) = y(4,i-1) + 0.005111 * rand(1) + (2 * rand(1)-1) * 6.95 / 280;
end;
plot(x,y(1,:));hold on;
plot(x,y(2,:));hold on;
plot(x,y(3,:));hold on;
plot(x,y(4,:));
